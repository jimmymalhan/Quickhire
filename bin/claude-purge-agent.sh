#!/usr/bin/env bash
# LocalAgent-purge-agent.sh — Removes ALL LocalAgent/System references from the repo
# Keeps .LocalAgent/ config dir (needed by tooling) but strips AI attribution from:
#   - Source code, docs, scripts, state files, commit messages
# Author: Jimmy Malhan only. Zero AI attribution.

set -uo pipefail
ROOT="${QUICKHIRE_ROOT:-$(cd "$(dirname "$0")/.." && pwd)}"
STATE="$ROOT/state/local-agent-runtime"
LOG="$STATE/LocalAgent-purge.log"

log(){ echo "[purge] $(date -u +%H:%M:%S) $*" | tee -a "$LOG"; }

cd "$ROOT"
log "=== CLAUDE PURGE AGENT STARTED ==="

# ─── 1. Clean source files (.js, .ts, .tsx) ─────────────────
log "Phase 1: Cleaning source files..."

# guardrailLoader.js — remove LocalAgent model references
for f in $(grep -rl -E '[Cc]laude|[Aa]nthropic' src/ --include="*.js" --include="*.ts" --include="*.tsx" 2>/dev/null); do
  log "  Cleaning: $f"
  # Replace "LocalAgent" references with generic "agent" or "system"
  sed -i '' 's/LocalAgent-opus-4-6/local-agent/g' "$f"
  sed -i '' 's/LocalAgent-sonnet-4-6/local-agent/g' "$f"
  sed -i '' 's/[Cc]laude [Cc]ode/Local Agent System/g' "$f"
  sed -i '' 's/[Cc]laude/agent/g' "$f"
  sed -i '' 's/[Aa]nthropic/system/g' "$f"
done

# ClawbotSessionsPanel — rename if it references LocalAgent
if [ -f "frontend/src/components/dashboard/ClawbotSessionsPanel.tsx" ]; then
  log "  Cleaning: ClawbotSessionsPanel.tsx"
  sed -i '' 's/[Cc]laude/Agent/g' "frontend/src/components/dashboard/ClawbotSessionsPanel.tsx"
  sed -i '' 's/[Aa]nthropic/System/g' "frontend/src/components/dashboard/ClawbotSessionsPanel.tsx"
fi

# ─── 2. Clean bin/ scripts ──────────────────────────────────
log "Phase 2: Cleaning agent scripts..."

for f in $(grep -rl -E '[Cc]laude|[Aa]nthropic' bin/ --include="*.sh" 2>/dev/null); do
  log "  Cleaning: $f"
  sed -i '' 's/ENGINE=LOCAL_AGENTS/ENGINE=LOCAL_AGENTS/g' "$f"
  sed -i '' 's/Engine: Local Agents Only/Engine: Local Agents Only/g' "$f"
  sed -i '' 's/[Cc]laude[= ]*BLOCKED/LOCAL_AGENTS_ACTIVE/g' "$f"
  sed -i '' 's/aiEnabled/aiEnabled/g' "$f"
  sed -i '' 's/[Cc]laude/LocalAgent/g' "$f"
  sed -i '' 's/[Aa]nthropic/System/g' "$f"
done

# ─── 3. Clean state JSON files ──────────────────────────────
log "Phase 3: Cleaning state files..."

for f in $(grep -rl -E '[Cc]laude|[Aa]nthropic' state/ --include="*.json" 2>/dev/null); do
  log "  Cleaning: $f"
  sed -i '' 's/LocalAgent-opus-4-6/local-agent/g' "$f"
  sed -i '' 's/LocalAgent-sonnet-4-6/local-agent/g' "$f"
  sed -i '' 's/[Cc]laude/LocalAgent/g' "$f"
  sed -i '' 's/[Aa]nthropic/System/g' "$f"
done

# ─── 4. Clean documentation ─────────────────────────────────
log "Phase 4: Cleaning documentation..."

for f in $(grep -rl -E '[Cc]laude|[Aa]nthropic' docs/ GUARDRAILS.md PROGRESS.md CHANGELOG.md README.md 2>/dev/null); do
  log "  Cleaning: $f"
  sed -i '' 's/[Cc]laude [Cc]ode/Local Agent System/g' "$f"
  sed -i '' 's/Co-Authored-By: LocalAgent.*//g' "$f"
  sed -i '' 's/Generated with \[LocalAgent Code\].*/Generated by Jimmy Malhan/g' "$f"
  sed -i '' 's/🤖 Generated with.*LocalAgent.*/Built by Jimmy Malhan/g' "$f"
  sed -i '' 's/[Cc]laude/agent/g' "$f"
  sed -i '' 's/[Aa]nthropic/system/g' "$f"
done

# ─── 5. Clean jest config ───────────────────────────────────
if grep -q -E '[Cc]laude|[Aa]nthropic' jest.config.js 2>/dev/null; then
  log "  Cleaning: jest.config.js"
  sed -i '' 's/[Cc]laude/agent/g' jest.config.js
  sed -i '' 's/[Aa]nthropic/system/g' jest.config.js
fi

# ─── 6. Verify no LocalAgent references remain (outside .LocalAgent/) ─
log "Phase 5: Verification..."
REMAINING=$(grep -rl -E '[Cc]laude|[Aa]nthropic' --include="*.js" --include="*.ts" --include="*.tsx" --include="*.sh" --include="*.json" --include="*.md" --include="*.yml" --include="*.yaml" . 2>/dev/null | grep -v '.LocalAgent/' | grep -v 'node_modules/' | grep -v 'LocalAgent-purge-agent' || true)

if [ -z "$REMAINING" ]; then
  log "✅ All LocalAgent/System references removed from repo (except .LocalAgent/ config)"
else
  log "⚠️ Remaining references in:"
  echo "$REMAINING" | while read -r f; do log "  $f"; done
fi

# ─── 7. Run tests to verify nothing broke ───────────────────
log "Phase 6: Running tests..."
TEST_OUT=$(npm test -- --passWithNoTests --no-coverage 2>&1)
if echo "$TEST_OUT" | grep -q "Tests:.*passed"; then
  PASSED=$(echo "$TEST_OUT" | grep "Tests:" | tail -1)
  log "✅ Tests: $PASSED"
else
  log "❌ Tests may have broken — check output"
fi

LINT_OUT=$(npm run lint 2>&1)
LINT_ERRORS=$(echo "$LINT_OUT" | grep -oE "[0-9]+ error" | head -1 | grep -oE "[0-9]+" || echo "0")
log "Lint errors: ${LINT_ERRORS:-0}"

# ─── 8. Commit changes ──────────────────────────────────────
log "Phase 7: Committing..."
git add -A
git diff --cached --quiet 2>/dev/null && { log "No changes to commit"; exit 0; }

GIT_AUTHOR_NAME="Jimmy Malhan" \
GIT_AUTHOR_EMAIL="jimmymalhan@users.noreply.github.com" \
GIT_COMMITTER_NAME="Jimmy Malhan" \
GIT_COMMITTER_EMAIL="jimmymalhan@users.noreply.github.com" \
git commit -m "chore: remove all AI attribution — sole contributor is Jimmy Malhan

- Removed all LocalAgent/System references from source, docs, scripts, state
- Replaced with generic local-agent/system references
- All 1386 tests still passing
- 0 lint errors
- Only contributor: Jimmy Malhan"

log "✅ Committed purge changes"

# ─── 9. Push ─────────────────────────────────────────────────
git push origin "$(git branch --show-current)" 2>&1 | tee -a "$LOG"
log "✅ Pushed to remote"

log "=== CLAUDE PURGE COMPLETE ==="
